
public class Book {
	private String title = " ";
	private String author = " ";
	public int year=0;
	String getTitle() {
		return title;
	}
	String getAuthor() {
		return author;
	}
	int getYear() {
		return year;
	}
	void setTitle(String title) {
		this.title=title;
	}
	void setAuthor(String author) {
		this.author=author;
	}
	void setYear(int year) {
		this.year=year;
	}
}
